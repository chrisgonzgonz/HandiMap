#import "SNRColor.h"

/// This class uses the following theme from Kuler:
/// \link https://color.adobe.com/Quiet-Cry-color-theme-124171/

@implementation SNRColor

+ (UIColor *)darkColor {
  return [UIColor colorWithRed:28 green:29 blue:33 alpha:1];
}

+ (UIColor *)grayColor {
  return [UIColor colorWithRed:49 green:53 blue:61 alpha:1];
}

+ (UIColor *)lightColor {
  return [UIColor colorWithRed:238 green:239 blue:247 alpha:1];
}

+ (UIColor *)mainColor {
  return [UIColor colorWithRed:68 green:88 blue:120 alpha:1];
}

+ (UIColor *)highlightColor {
  return [UIColor colorWithRed:146 green:205 blue:207 alpha:1];
}

@end
